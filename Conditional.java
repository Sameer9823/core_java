public class Conditional {
    public static void main(String[] args) {
        int x = 8;

        if(x > 6){
            System.out.println("x is greater than 6");
        } else if(x == 6) {
            System.out.println("x is equal to 6");
        } else {
            System.out.println("x is less than 6");
        }
    }
}
